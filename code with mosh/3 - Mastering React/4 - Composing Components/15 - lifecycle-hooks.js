/*
    Every React Component face a lifecyle...
    The lifecycle has 3 phases:

    - Mount     ---> This phase is when the react component is created and inserted into the DOM
    - Update    ---> This phase is when the react component is updated
    - Unmount   ---> This phase is when the react component is removed from the DOM

    *** Mount Phase ***
    This phase has 3 methods that are called in order....
    1.- constructor
    2.- render
    3.- componentDidMount

    *** Update Phase ***
    This phase has 2 methods that are called in order....
    1.- render
    2.- componentDidUpdate

    *** Unmount Phase ***
    This phase has 1 method....
    1.- componentWillUnmount

*/
